public class penduduk {
    private String nama;
    private int pendapatan;

    public penduduk() {
    }

    public penduduk(String nama, int pendapatan) {
        this.nama = nama;
        this.pendapatan = pendapatan;
    }

    public String getNama() {
        return nama;
    }

    public int getPendapatan() {
        return pendapatan;
    }

}
